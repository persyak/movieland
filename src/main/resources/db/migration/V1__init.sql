CREATE TABLE IF NOT EXISTS movieland.movie (
  id SERIAL PRIMARY KEY NOT NULL,
  name_ua VARCHAR(100) NOT NULL,
  name_native VARCHAR(100) NOT NULL,
  year_of_release DATE NOT NULL,
  description VARCHAR(1000) NOT NULL,
  rating double precision NOT NULL,
  price double precision NOT NULL,
  picture_path VARCHAR(500) NOT NULL,
  votes INT NOT NULL
);

CREATE TABLE IF NOT EXISTS movieland.genre (
  id SERIAL PRIMARY KEY NOT NULL,
  name VARCHAR(45) NOT NULL
);

CREATE TABLE IF NOT EXISTS movieland.movie_genre (
  movie_id INT NOT NULL,
  genre_id INT NOT NULL,
  CONSTRAINT movie_id
    FOREIGN KEY (movie_id)
    REFERENCES movieland.movie (id)
    ON DELETE CASCADE
    ON UPDATE NO ACTION,
  CONSTRAINT genre_id
    FOREIGN KEY (genre_id)
    REFERENCES movieland.genre (id)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);

CREATE INDEX movie_id_idx ON movieland.movie_genre
(movie_id ASC);

CREATE INDEX genre_id_idx ON movieland.movie_genre
(genre_id ASC);

CREATE TABLE IF NOT EXISTS movieland.user (
  id SERIAL PRIMARY KEY NOT NULL,
  nickname VARCHAR(45) NOT NULL,
  email VARCHAR(45) NOT NULL,
  password BYTEA NOT NULL,
  type VARCHAR(1) NOT NULL DEFAULT 'U'
  );

CREATE TABLE IF NOT EXISTS movieland.review (
  id SERIAL PRIMARY KEY NOT NULL,
  movie_id INT NOT NULL,
  user_id INT NOT NULL,
  description VARCHAR(500) NOT NULL,
  CONSTRAINT review_user_id
    FOREIGN KEY (user_id)
    REFERENCES movieland.user (id)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT review_movie_id
    FOREIGN KEY (movie_id)
    REFERENCES movieland.movie (id)
    ON DELETE CASCADE
    ON UPDATE NO ACTION
);

CREATE INDEX user_id_idx ON movieland.review
(user_id ASC);

CREATE INDEX movie_id_idx_review ON movieland.review
(movie_id ASC);

CREATE TABLE IF NOT EXISTS movieland.country (
  id SERIAL PRIMARY KEY NOT NULL,
  name VARCHAR(45) NOT NULL
);

CREATE TABLE IF NOT EXISTS movieland.movie_country (
  movie_id INT NOT NULL,
  country_id INT NOT NULL,
  CONSTRAINT movie_country_country_id
    FOREIGN KEY (country_id)
    REFERENCES movieland.country (id)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT movie_country_movie_id
    FOREIGN KEY (movie_id)
    REFERENCES movieland.movie (id)
    ON DELETE CASCADE
    ON UPDATE NO ACTION);

CREATE INDEX country_id_idx ON movieland.movie_country
(country_id ASC);

CREATE INDEX movie_id_idx_movie_country ON movieland.movie_country
(movie_id ASC);

CREATE TABLE IF NOT EXISTS movieland.rating (
  user_id INT NOT NULL,
  movie_id INT NOT NULL,
  rating double precision NOT NULL,
  CONSTRAINT rating_user_id
    FOREIGN KEY (user_id)
    REFERENCES movieland.user (id)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION,
  CONSTRAINT rating_movie_id
    FOREIGN KEY (movie_id)
    REFERENCES movieland.movie (id)
    ON DELETE CASCADE
    ON UPDATE NO ACTION);

CREATE INDEX movie_id_idx_rating ON movieland.rating
(movie_id ASC);

CREATE INDEX user_id_idx_rating ON movieland.rating
(user_id ASC);

